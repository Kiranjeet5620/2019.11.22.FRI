//program to add numbers from 100 to 1 using for loop

var i=0;sum=0;
for(i=100;i>=1;i--){
    sum=sum+i;
}
console.log("Sum of numbers from 100 to 1 is :"+sum)

//program to add numbers from 500to 114 using for loop

var j=500;add=0;
while(j>=114){
    add=add+j;
    j--;
}
console.log("Sum of numbers from 500 to 114 is :"+add)
